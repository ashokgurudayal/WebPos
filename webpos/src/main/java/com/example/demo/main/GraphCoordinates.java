package com.example.demo.main;

public class GraphCoordinates {
	String x;
	double y;
	public GraphCoordinates(String i, double d) {
		this.x = i;
		this.y = d;
	}
	
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
